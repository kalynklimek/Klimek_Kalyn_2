public class ASCIIArt
{
	public static void main(String[]args)
	{
		System.out.println("(\\__/)");
		System.out.println("(=\'.\'=)");
		System.out.println("(\")_(\")");

		System.out.println("             __           __    ");
		System.out.println("             /  \\~~~~/  \\   ");
		System.out.println("      ,----(      ..        )  ");
		System.out.println("     /       \\__       __/   ");
		System.out.println("    /|          (\\    |(     ");
		System.out.println("   ^ \\     /__ \\   /\\ |      ");
		System.out.println("       |__|    |__|-\"        ");
	}
}